    private void configurarTabla() {
        modeloTabla = new DefaultTableModel(new Object[]{"ID", "Nombre"}, 0);
        tablaDepartamentos.setModel(modeloTabla);

        tablaDepartamentos.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila = tablaDepartamentos.getSelectedRow();
                if (fila >= 0) {
                    txtNombre.setText(modeloTabla.getValueAt(fila, 1).toString());
                }
            }
        });
    }

    private void actualizarTabla() {
        modeloTabla.setRowCount(0);
        for (Departamento d : departamentoController.getDepartamentos()) {
            modeloTabla.addRow(new Object[]{d.getId(), d.getNombre()});
        }
    }

